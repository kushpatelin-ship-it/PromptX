// EMPLOYEE CARD 6-METRIC UPGRADE
// Replaces the default pcm metrics section with 6 unique metrics
// Injected via gemini-features.js to avoid dashboard string escaping
// ══════════════════════════════════════════════════════════════════════════════

function upgradePerfMetrics(){
  var grid = document.getElementById("pcg");
  if(!grid) return;

  // Find all employee cards and upgrade their metrics section
  var cards = grid.querySelectorAll(".pcc");
  cards.forEach(function(card){
    var pcm = card.querySelector(".pcm");
    if(!pcm) return;

    // Get data from existing met divs
    var mets = pcm.querySelectorAll(".met");
    var data = {};
    mets.forEach(function(m){
      var lbl = m.querySelector(".ml");
      var val = m.querySelector(".mv");
      if(lbl && val) data[lbl.textContent.trim()] = val.textContent.trim();
    });

    // Build 6-metric grid
    var prod = parseFloat(data["Productive"])||0;
    var active = data["Active"]||"n/a";
    var idle = data["Idle"]||"n/a";
    var spoof = parseInt(data["Spoof"])||0;

    // Get hostname for lookup
    var hostnameLink = card.querySelector(".pcn a,.pcn");
    var hostname = hostnameLink ? hostnameLink.textContent.replace(/\s+/g,"").split(" ")[0] : "";

    // Create new metrics grid
    var newGrid = document.createElement("div");
    newGrid.style.cssText = "display:grid;grid-template-columns:repeat(3,1fr);gap:5px;margin:10px 0";
    newGrid.className = "pcm-upgraded";

    function mkCard(label, value, color, warn){
      var border = warn==="danger"?"rgba(255,61,87,.5)":warn==="warn"?"rgba(255,171,0,.4)":"#1a2332";
      var d = document.createElement("div");
      d.style.cssText = "background:rgba(255,255,255,.03);border-radius:6px;padding:7px;border:1px solid "+border+";position:relative";
      d.innerHTML = "<div style='font-size:8px;font-weight:700;text-transform:uppercase;color:#4a6080;margin-bottom:3px'>"+label+"</div>"
        +"<div style='font-size:13px;font-weight:800;color:"+color+"'>"+value+"</div>";
      return d;
    }

    // 1. Productive %
    var prodColor = prod>=70?"#00e676":prod>=40?"#ffab00":"#ff3d57";
    var prodWarn = prod<40?"danger":prod<70?"warn":"";
    newGrid.appendChild(mkCard("Productive", prod+"%", prodColor, prodWarn));

    // 2. Active time
    newGrid.appendChild(mkCard("Active Time", active, "#00e676", ""));

    // 3. Idle time
    newGrid.appendChild(mkCard("Idle Time", idle, "#ffab00", ""));

    // 4. Away times (from card counter data)
    var awayVal = data["Away"]||"n/a";
    newGrid.appendChild(mkCard("Away", awayVal, "#4a6080", ""));

    // 5. Locked times
    var lockedVal = data["Locked"]||"n/a";
    newGrid.appendChild(mkCard("Locked", lockedVal, "#2979ff", ""));

    // 6. Spoof detect
    var spoofColor = spoof>0?"#ff3d57":"#00e676";
    var spoofWarn = spoof>0?"danger":"";
    newGrid.appendChild(mkCard("Spoof Detect", ""+spoof, spoofColor, spoofWarn));

    // Replace old pcm
    pcm.parentNode.replaceChild(newGrid, pcm);
  });
}

