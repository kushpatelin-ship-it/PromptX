var _prevStates={};
var _idleStart={};
function checkIdleReturn(kids){
  kids.forEach(function(emp){
    var hn=emp.hostname;
    var cur=_stOf(emp);
    var prev=_prevStates[hn];
    if((prev==="idle"||prev==="away")&&cur==="active"){
      var dur=_idleStart[hn]?Math.round((Date.now()-_idleStart[hn])/60000):0;
      if(dur>=2)showIdlePopup(hn,dur);
    }
    if((cur==="idle"||cur==="away")&&(prev!=="idle"&&prev!=="away"))_idleStart[hn]=Date.now();
    _prevStates[hn]=cur;
  });
}

