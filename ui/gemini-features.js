// ── 7. PRODUCTIVITY MATRIX (improved calculation) ─────────────────────────────
function productivityMatrix(ds,sg){
  if(!ds)return null;
  var active=ds.active_minutes||0;
  var idle=ds.idle_minutes||0;
  var away=ds.away_minutes||0;
  var focus=ds.focus_score||0;
  var prod=ds.productive_pct||0;
  var longest=ds.longest_focus_minutes||0;
  var spoof=ds.spoofed_windows||0;
  var lateStart=sg&&sg.login_time&&sg.login_time>"09:30"?1:0;
  var idleRate=active+idle>0?Math.round(idle/(active+idle)*100):0;
  var focusDensity=active>0?Math.round(longest/active*100):0;
  var lostRatio=active>0?Math.round((ds.lost_time_minutes||0)/active*100):0;
  // Weighted score
  var score=Math.round(
    focus*0.35+
    prod*0.25+
    (100-idleRate)*0.20+
    Math.min(100,focusDensity)*0.10+
    (lateStart?85:100)*0.05+
    (spoof>0?70:100)*0.05
  );
  score=Math.max(0,Math.min(100,score));
  return {score:score,idleRate:idleRate,focusDensity:focusDensity,lostRatio:lostRatio,lateStart:!!lateStart};
}

