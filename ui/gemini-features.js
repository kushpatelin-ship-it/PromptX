var _idleHighCpuStart = null;
var _idleHighCpuMaxCpu = 0;
var _reclaimerShown = {};

function checkActivityReclaimer(kids){
  var now = Date.now();
  kids.forEach(function(emp){
    var hn = emp.hostname;
    var cur = _stOf(emp);
    var prev = _prevStates[hn];
    var ds = emp.summary;

    // Detect when idle/away with high CPU
    if((cur === "idle" || cur === "away") && !_idleHighCpuStart){
      _idleHighCpuStart = now;
      _idleHighCpuMaxCpu = 0;
    }

    // Track max CPU during idle period
    if((cur === "idle" || cur === "away") && _idleHighCpuStart){
      fetch("/api/sysmetrics").then(function(r){return r.json();}).then(function(d){
        if(d.cpu_pct && d.cpu_pct > _idleHighCpuMaxCpu) _idleHighCpuMaxCpu = d.cpu_pct;
      }).catch(function(){});
    }

    // When returning from 10m+ idle with high CPU detected
    if((prev === "idle" || prev === "away") && cur === "active" && _idleHighCpuStart){
      var idleDur = Math.round((now - _idleHighCpuStart) / 60000);
      var maxCpu = _idleHighCpuMaxCpu;
      _idleHighCpuStart = null;
      _idleHighCpuMaxCpu = 0;

      if(idleDur >= 10 && maxCpu >= 50){
        var rKey = hn + "_" + Math.floor(now / 3600000);
        if(!_reclaimerShown[rKey]){
          _reclaimerShown[rKey] = true;
          showReclaimerPopup(hn, idleDur, maxCpu);
        }
      }
    }
  });
}

